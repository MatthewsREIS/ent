func TestEdgeSchemaEntQL(t *testing.T) {
	client, err := ent.Open(dialect.SQLite, "file:ent?mode=memory&cache=shared&_fk=1")
	require.NoError(t, err)
	defer client.Close()
	ctx := context.Background()
	require.NoError(t, client.Schema.Create(ctx, migrate.WithGlobalUniqueID(true)))

	tweets := client.Tweet.CreateBulk(
		client.Tweet.Create().SetText("t1"),
		client.Tweet.Create().SetText("t2"),
	).SaveX(ctx)
	nat := client.User.Create().SetName("nati").SaveX(ctx)
	a8m := client.User.Create().SetName("a8m").AddLikedTweetIDs(tweets[0].ID, tweets[1].ID).SaveX(ctx)

	// Using the regular fluent API.
	require.Equal(t, a8m.ID, client.User.Query().Where(user.HasLikes()).OnlyIDX(ctx))
	require.Equal(t, nat.ID, client.User.Query().Where(user.Not(user.HasLikes())).OnlyIDX(ctx))

	// Using EntQL.
	q1, q2 := client.User.Query(), client.User.Query()
	q1.Filter().WhereHasLikes()
	q2.Filter().Where(entql.Not(entql.HasEdge("likes")))
	require.Equal(t, a8m.ID, q1.OnlyIDX(ctx))
	require.Equal(t, nat.ID, q2.OnlyIDX(ctx))

	client.User.UpdateOne(nat).AddLikedTweetIDs(tweets[0].ID).ExecX(ctx)
	// Using the regular fluent API.
	require.Equal(t, 2, client.User.Query().Where(user.HasLikesWith(tweetlike.TweetID(tweets[0].ID))).CountX(ctx))
	require.Equal(t, 1, client.User.Query().Where(user.HasLikesWith(tweetlike.TweetID(tweets[1].ID))).CountX(ctx))
	// Using EntQL.
	q1, q2 = client.User.Query(), client.User.Query()
	q1.Filter().WhereHasLikesWith(tweetlike.TweetID(tweets[0].ID))
	q2.Filter().Where(entql.HasEdgeWith("likes", entql.FieldEQ(tweetlike.FieldTweetID, tweets[0].ID)))
	require.Equal(t, 2, q1.CountX(ctx))
	require.Equal(t, 2, q2.CountX(ctx))
	q1, q2 = client.User.Query(), client.User.Query()
	q1.Filter().WhereHasLikesWith(tweetlike.TweetID(tweets[1].ID))
	q2.Filter().Where(entql.HasEdgeWith("likes", entql.FieldEQ(tweetlike.FieldTweetID, tweets[1].ID)))
	require.Equal(t, 1, q1.CountX(ctx))
	require.Equal(t, 1, q2.CountX(ctx))

	u1 := client.User.Create().SetName("u1").SaveX(ctx)
	u2 := client.User.Create().SetName("u2").AddRelativeIDs(u1.ID).SaveX(ctx)
	client.User.Create().SetName("u3").AddRelativeIDs(u2.ID).ExecX(ctx)
	require.Equal(t, 4, client.Relationship.Query().CountX(ctx))
	require.Zero(t, client.Relationship.Query().Where(relationship.HasInfo()).CountX(ctx))
	ri := client.RelationshipInfo.Create().SetText("parent").SaveX(ctx)
	r := client.Relationship.Query().FirstX(ctx)
	rl1 := client.Relationship.UpdateOne(r).SetInfoID(ri.ID).SaveX(ctx)
	require.Equal(t, 1, client.Relationship.Query().Where(relationship.HasInfo()).CountX(ctx))
	// Using EntQL.
	q3 := client.Relationship.Query()
	q3.Filter().WhereHasInfoWith(relationshipinfo.ID(ri.ID))
	rl2 := q3.OnlyX(ctx)
	require.Equal(t, rl1.UserID, rl2.UserID)
	require.Equal(t, rl1.RelativeID, rl2.RelativeID)
	require.Equal(t, rl1.InfoID, rl2.InfoID)
}

func TestEdgeSchemaTypeMatching(t *testing.T) {
	client, err := ent.Open(dialect.SQLite, "file:ent?mode=memory&_fk=1")
	require.NoError(t, err)
	defer client.Close()
	ctx := context.Background()
	require.NoError(t, client.Schema.Create(ctx, migrate.WithGlobalUniqueID(true)))

	files := client.File.CreateBulk(
		client.File.Create().SetName("a"),
		client.File.Create().SetName("b"),
		client.File.Create().SetName("c"),
	).SaveX(ctx)
	proc := client.Process.Create().AddFileIDs(files[0].ID, files[1].ID).SaveX(ctx)

	// Assoc query.
	require.Equal(t, []string{"a", "b"}, client.Process.QueryFiles(proc).Order(ent.Asc(file.FieldName)).Select(file.FieldName).StringsX(ctx))
	require.Equal(t, []int{files[0].ID, files[1].ID}, client.Process.QueryAttachedFiles(proc).Order(ent.Asc(attachedfile.FieldFID)).Select(attachedfile.FieldFID).IntsX(ctx))
	require.Equal(t, []int{files[0].ID, files[1].ID}, client.Process.QueryAttachedFiles(proc).QueryFi().IDsX(ctx))
	require.Equal(t, proc.ID, client.Process.Query().QueryAttachedFiles().QueryProc().OnlyIDX(ctx))

	// Inverse query.
	require.Equal(t, proc.ID, client.File.QueryProcesses(files[0]).OnlyIDX(ctx))
	require.Equal(t, proc.ID, client.File.QueryProcesses(files[1]).OnlyIDX(ctx))
	require.False(t, client.File.QueryProcesses(files[2]).ExistX(ctx))
	require.Equal(t, []int{files[0].ID, files[1].ID}, client.File.Query().QueryProcesses().QueryFiles().IDsX(ctx))
	require.Equal(t, []int{files[0].ID, files[1].ID}, client.File.Query().QueryProcesses().QueryAttachedFiles().QueryFi().IDsX(ctx))

	// Edge schema query and mutation.
	require.Equal(t, []int{files[0].ID, files[1].ID}, client.AttachedFile.Query().QueryFi().IDsX(ctx))
	require.Equal(t, []int{files[0].ID, files[1].ID}, client.AttachedFile.Query().QueryProc().QueryFiles().IDsX(ctx))
	require.Equal(t, proc.ID, client.AttachedFile.Query().QueryProc().OnlyIDX(ctx))
	af := client.AttachedFile.Create().SetFiID(files[2].ID).SetProcID(proc.ID).SaveX(ctx)
	require.Equal(t, proc.ID, client.AttachedFile.QueryProc(af).OnlyIDX(ctx))
	require.Equal(t, files[2].ID, client.AttachedFile.QueryFi(af).OnlyIDX(ctx))
	require.Equal(t, []int{files[0].ID, files[1].ID, files[2].ID}, client.AttachedFile.Query().QueryFi().IDsX(ctx))
	require.Equal(t, files[2].ID, client.AttachedFile.Query().QueryProc().QueryFiles().Where(file.Name(files[2].Name)).OnlyIDX(ctx))
}

// TestEntQLNonEdgePredicates covers entql typed filter methods on non-edge
// entities — regression net for Phase 3's where.tmpl rewrite, which feeds
// entql.go generation.
func TestEntQLNonEdgePredicates(t *testing.T) {
	client, err := ent.Open(dialect.SQLite, "file:ent?mode=memory&cache=shared&_fk=1")
	require.NoError(t, err)
	defer client.Close()
	ctx := context.Background()
	require.NoError(t, client.Schema.Create(ctx, migrate.WithGlobalUniqueID(true)))

	// Seed data: two groups, two roles, two tweets, two users.
	hub := client.Group.Create().SetName("hub").SaveX(ctx)
	lab := client.Group.Create().SetName("lab").SaveX(ctx)
	adminRole := client.Role.Create().SetName("admin").SaveX(ctx)
	client.Role.Create().SetName("member").ExecX(ctx)
	t1 := client.Tweet.Create().SetText("hello world").SaveX(ctx)
	t2 := client.Tweet.Create().SetText("goodbye world").SaveX(ctx)
	a8m := client.User.Create().SetName("a8m").AddLikedTweetIDs(t1.ID).SaveX(ctx)
	client.User.Create().SetName("nati").AddLikedTweetIDs(t1.ID, t2.ID).ExecX(ctx)

	t.Run("GroupFilter_WhereName", func(t *testing.T) {
		q := client.Group.Query()
		q.Filter().WhereName(entql.StringEQ(hub.Name))
		result := q.AllX(ctx)
		require.Len(t, result, 1)
		require.Equal(t, hub.ID, result[0].ID)
	})

	t.Run("GroupFilter_WhereName_ExcludesOther", func(t *testing.T) {
		q := client.Group.Query()
		q.Filter().WhereName(entql.StringEQ(lab.Name))
		result := q.AllX(ctx)
		require.Len(t, result, 1)
		require.Equal(t, lab.ID, result[0].ID)
	})

	t.Run("RoleFilter_WhereName", func(t *testing.T) {
		q := client.Role.Query()
		q.Filter().WhereName(entql.StringEQ(adminRole.Name))
		result := q.AllX(ctx)
		require.Len(t, result, 1)
		require.Equal(t, adminRole.ID, result[0].ID)
	})

	t.Run("TweetFilter_WhereText", func(t *testing.T) {
		q := client.Tweet.Query()
		q.Filter().WhereText(entql.StringEQ(t1.Text))
		result := q.AllX(ctx)
		require.Len(t, result, 1)
		require.Equal(t, t1.ID, result[0].ID)
	})

	t.Run("TweetFilter_WhereText_NEQ", func(t *testing.T) {
		// t2 has different text from t1; StringNEQ should exclude t1 and return t2.
		q := client.Tweet.Query()
		q.Filter().WhereText(entql.StringNEQ(t1.Text))
		result := q.AllX(ctx)
		require.Len(t, result, 1)
		require.Equal(t, t2.ID, result[0].ID)
	})

	t.Run("TweetFilter_WhereHasLikesWith", func(t *testing.T) {
		// t2 was liked by nati (user ID). t1 was liked by both.
		// Filter tweets that have a like from user a8m via the TweetLike edge.
		q := client.Tweet.Query()
		q.Filter().WhereHasLikesWith(tweetlike.UserID(a8m.ID))
		result := q.AllX(ctx)
		require.Len(t, result, 1, "only t1 was liked by a8m")
		require.Equal(t, t1.ID, result[0].ID)
	})

	t.Run("UserFilter_WhereName", func(t *testing.T) {
		q := client.User.Query()
		q.Filter().WhereName(entql.StringEQ("a8m"))
		result := q.AllX(ctx)
		require.Len(t, result, 1)
		require.Equal(t, a8m.ID, result[0].ID)
	})

	t.Run("UserFilter_WhereHasLikedTweetsWith", func(t *testing.T) {
		// Only nati liked t2.
		q := client.User.Query()
		q.Filter().WhereHasLikedTweetsWith(tweet.ID(t2.ID))
		result := q.AllX(ctx)
		require.Len(t, result, 1, "only nati liked t2")
		require.Equal(t, "nati", result[0].Name)
	})
}
