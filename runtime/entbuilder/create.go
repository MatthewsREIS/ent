package entbuilder

import (
	"context"
	"database/sql/driver"
	"fmt"

	"entgo.io/ent/dialect/sql/sqlgraph"
	"entgo.io/ent/schema/field"
)

// CreateDescriptor describes how to materialize an entity instance and its
// associated sqlgraph.CreateSpec from a mutation. Generated code is expected to
// provide the concrete functions and metadata for a particular entity.
type CreateDescriptor[C any, N any, M any] struct {
	// Table is the runtime table backing the entity.
	Table string
	// NewNode allocates a fresh entity instance seeded with the provided config.
	NewNode func(C) *N
	// ID holds metadata and callbacks for the entity identifier. It can be nil
	// for entities without a dedicated ID column (e.g. composite IDs).
	ID *IDDescriptor[C, N, M]
	// Fields contains the concrete mappings from mutation setters to column
	// assignments. The runtime loops over these bindings to populate both the
	// sqlgraph spec and the in-memory node.
	Fields []FieldDescriptor[C, N, M]
	// Edges captures outbound edge bindings. Build is evaluated lazily and can
	// skip appending specs by returning ok=false.
	Edges []EdgeDescriptor[C, N, M]
	// Factory allows descriptors to delegate spec construction to a custom
	// function (for example, existing template logic) while still leveraging the
	// generic runtime plumbing. When provided, the Fields and Edges collections
	// are ignored.
	Factory func(C, M) (*N, *sqlgraph.CreateSpec, error)
	// SpecModifiers allows templates (including extension hooks) to mutate the
	// constructed CreateSpec prior to execution. They are invoked in order.
	SpecModifiers []SpecModifier[C, N, M]
	// AfterSave hooks run after the CreateSpec has been executed successfully.
	// They receive the final spec (with populated ID.Value) in case additional
	// bookkeeping is required.
	AfterSave []AfterCreateFunc[C, N, M]
}

// FieldValue bundles the database value used for SQL persistence together with
// the typed value that should be reflected onto the generated entity struct.
// Generated code is responsible for populating both sides consistently.
type FieldValue struct {
	Spec driver.Value
	Node any
}

// FieldDescriptor binds a mutation getter to a column assignment.
type FieldDescriptor[C any, N any, M any] struct {
	Column string
	Type   field.Type
	// Value extracts the concrete column value from the mutation. ok reports
	// whether the field was set. For nillable fields the generator should set
	// ok=false when the value is absent.
	Value func(M) (value FieldValue, ok bool, err error)
	// Assign writes the materialized value to the in-memory node. For nillable
	// fields this function should perform the necessary pointer wrapping.
	Assign func(*N, FieldValue) error
}

// EdgeValue ties the constructed sqlgraph.EdgeSpec with additional metadata the
// Assign callback might require (for example, the typed IDs that were attached
// to the mutation prior to conversion to driver values).
type EdgeValue struct {
	Spec  *sqlgraph.EdgeSpec
	Nodes any
}

// EdgeDescriptor binds a mutation edge setter to a sqlgraph.EdgeSpec.
type EdgeDescriptor[C any, N any, M any] struct {
	// Value materializes the edge spec. ok reports whether the edge should be
	// appended to the CreateSpec.
	Value func(C, M) (EdgeValue, bool, error)
	// Assign applies any in-memory bookkeeping on the node (e.g. setting the
	// foreign-key field on the entity struct for O2O relations owned by this
	// table).
	Assign func(*N, EdgeValue) error
}

// SpecModifier mutates the CreateSpec before execution. Returning an error
// aborts the save operation.
type SpecModifier[C any, N any, M any] func(spec *sqlgraph.CreateSpec, cfg C, mutation M) error

// AfterCreateFunc runs after the CreateSpec was persisted successfully.
type AfterCreateFunc[C any, N any, M any] func(ctx context.Context, cfg C, mutation M, spec *sqlgraph.CreateSpec, node *N) error

// IDDescriptor holds metadata and callbacks for identifier management.
type IDDescriptor[C any, N any, M any] struct {
	Column string
	Type   field.Type
	// UserDefined reports whether the ID is supplied by the mutation rather
	// than generated by the database.
	UserDefined bool
	// Value returns the mutation-provided identifier. ok reports whether a value
	// exists. For optional user-defined IDs this allows skipping assignments.
	Value func(M) (value FieldValue, ok bool, err error)
	// AssignMutation is invoked when Value reports ok=true, allowing the
	// generator to reflect the ID back into the mutation (e.g. to store
	// pointers for later access).
	AssignMutation func(M, FieldValue)
	// AssignNode stores the identifier on the in-memory node. It is invoked both
	// for mutation-provided IDs and generated IDs (after persistence) if
	// AssignGenerated is nil.
	AssignNode func(*N, FieldValue) error
	// AssignGenerated is invoked after the CreateSpec executed successfully and
	// the driver populated spec.ID.Value. When nil, AssignNode is reused.
	AssignGenerated func(*N, driver.Value) error
	// FromSpec converts a driver.Value (coming from spec.ID.Value) into the
	// structured representation expected by AssignNode / AssignMutation.
	FromSpec func(driver.Value) (FieldValue, error)
}

// BuildCreateSpec constructs a node instance and a sqlgraph.CreateSpec from the
// provided mutation using the descriptor metadata.
func BuildCreateSpec[C any, N any, M any](cfg C, mutation M, desc *CreateDescriptor[C, N, M]) (*N, *sqlgraph.CreateSpec, error) {
	if desc == nil {
		return nil, nil, fmt.Errorf("entbuilder: missing create descriptor")
	}

	var (
		node *N
		spec *sqlgraph.CreateSpec
		err  error
	)

	if desc.Factory != nil {
		node, spec, err = desc.Factory(cfg, mutation)
		if err != nil {
			return nil, nil, err
		}
		if spec == nil {
			return nil, nil, fmt.Errorf("entbuilder: factory returned nil create spec for table %q", desc.Table)
		}
	} else {
		if desc.NewNode == nil {
			return nil, nil, fmt.Errorf("entbuilder: descriptor missing NewNode constructor")
		}
		node = desc.NewNode(cfg)

		var idSpec *sqlgraph.FieldSpec
		if desc.ID != nil && desc.ID.Column != "" {
			idSpec = sqlgraph.NewFieldSpec(desc.ID.Column, desc.ID.Type)
		}
		spec = sqlgraph.NewCreateSpec(desc.Table, idSpec)

		if desc.ID != nil && desc.ID.UserDefined && desc.ID.Value != nil {
			if fv, ok, err := desc.ID.Value(mutation); err != nil {
				return nil, nil, err
			} else if ok {
				spec.ID.Value = fv.Spec
				if desc.ID.AssignMutation != nil {
					desc.ID.AssignMutation(mutation, fv)
				}
				if desc.ID.AssignNode != nil {
					if err := desc.ID.AssignNode(node, fv); err != nil {
						return nil, nil, err
					}
				}
			}
		}

		for _, field := range desc.Fields {
			if field.Value == nil {
				continue
			}
			fv, ok, err := field.Value(mutation)
			if err != nil {
				return nil, nil, err
			}
			if !ok {
				continue
			}
			spec.SetField(field.Column, field.Type, fv.Spec)
			if field.Assign != nil {
				if err := field.Assign(node, fv); err != nil {
					return nil, nil, err
				}
			}
		}

		for _, edge := range desc.Edges {
			if edge.Value == nil {
				continue
			}
			ev, ok, err := edge.Value(cfg, mutation)
			if err != nil {
				return nil, nil, err
			}
			if !ok || ev.Spec == nil {
				continue
			}
			spec.Edges = append(spec.Edges, ev.Spec)
			if edge.Assign != nil {
				if err := edge.Assign(node, ev); err != nil {
					return nil, nil, err
				}
			}
		}
	}

	for _, mod := range desc.SpecModifiers {
		if mod == nil {
			continue
		}
		if err := mod(spec, cfg, mutation); err != nil {
			return nil, nil, err
		}
	}

	return node, spec, nil
}

// ApplyGeneratedID reflects the driver-populated identifier back into the node
// (and optionally the mutation). It is safe to call when spec.ID or its value is
// nil.
func ApplyGeneratedID[C any, N any, M any](mutation M, spec *sqlgraph.CreateSpec, node *N, desc *CreateDescriptor[C, N, M]) error {
	if desc == nil || desc.ID == nil || spec == nil || spec.ID == nil || spec.ID.Value == nil {
		return nil
	}
	id := desc.ID
	if id.AssignGenerated != nil {
		return id.AssignGenerated(node, spec.ID.Value)
	}
	if id.AssignNode != nil {
		if id.FromSpec == nil {
			return fmt.Errorf("entbuilder: missing FromSpec converter for ID column %q", id.Column)
		}
		fv, err := id.FromSpec(spec.ID.Value)
		if err != nil {
			return err
		}
		return id.AssignNode(node, fv)
	}
	return nil
}

// RunAfterCreate executes the descriptor AfterSave callbacks.
func RunAfterCreate[C any, N any, M any](ctx context.Context, cfg C, mutation M, spec *sqlgraph.CreateSpec, node *N, desc *CreateDescriptor[C, N, M]) error {
	if desc == nil {
		return nil
	}
	for _, hook := range desc.AfterSave {
		if hook == nil {
			continue
		}
		if err := hook(ctx, cfg, mutation, spec, node); err != nil {
			return err
		}
	}
	return nil
}
